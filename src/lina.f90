!
!     function lina: find adresses in packed array
!
!     part of QMDFF
!
integer function lina(i1,i2)
integer::i1,i2,idum1,idum2

idum1=max(i1,i2)
idum2=min(i1,i2)
lina=idum2+idum1*(idum1-1)/2

return
end


