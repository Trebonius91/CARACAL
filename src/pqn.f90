!
!     function pqn: determines row in periodic system from element number!
!
!     part of QMDFF
!
integer function pqn(at)
integer::at

if (at.le.2)then
   pqn=1
else if (at.le.10) then
   pqn=2
else if (at.le.18) then
   pqn=3
else if (at.le.36) then
   pqn=4
else if (at.le.54) then
   pqn=5
else
   pqn=6
end if

end function pqn

